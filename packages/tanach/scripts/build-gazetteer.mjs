/**
 * Build the Tanach place gazetteer for the geography producer, from OpenBible's
 * public-domain Bible-Geocoding-Data (CC-BY). For every ancient place that
 * resolves to a coordinate (~1335 of 1342), record its name + every spelling
 * variant (friendly_id + translation_name_counts) and the point coordinate of
 * its best modern identification. Emits src/worker/gazetteer.ts: a compact
 * places table + an alias→index map, so the worker can deterministically turn
 * a place NAME (from the LLM) into {lat,lng} — grounding, not guessing.
 *
 * Run from packages/tanach: `node scripts/build-gazetteer.mjs`. Fetches at
 * build time; nothing but the generated output is committed.
 */

import { writeFileSync } from 'node:fs';

const BASE = 'https://raw.githubusercontent.com/openbibleinfo/Bible-Geocoding-Data/master/data';

async function jsonl(file) {
  const res = await fetch(`${BASE}/${file}`);
  if (!res.ok) throw new Error(`fetch ${file} failed ${res.status}`);
  const text = await res.text();
  return text
    .split('\n')
    .filter(Boolean)
    .map((l) => JSON.parse(l));
}

// Aggressive normalisation so spelling variants collapse: lowercase, drop
// everything but a-z0-9 ("Kiriath-Arba"/"Kiriath Arba" -> "kiriatharba",
// "Beer-sheba"/"Beersheba" -> "beersheba").
const norm = (s) => s.toLowerCase().replace(/[^a-z0-9]+/g, '');

// People are not places on a map; everything else with a point is fair game.
const SKIP_TYPES = new Set(['people group']);

const [ancient, modern] = await Promise.all([jsonl('ancient.jsonl'), jsonl('modern.jsonl')]);
const modernById = new Map(modern.map((m) => [m.id, m]));

function pointOf(m) {
  if (!m) return null;
  if (typeof m.lonlat === 'string') {
    const [lon, lat] = m.lonlat.split(',').map(Number);
    if (Number.isFinite(lon) && Number.isFinite(lat)) return [lon, lat];
  }
  const g = m.geometry;
  if (g && g.type === 'Point' && Array.isArray(g.coordinates)) {
    const [lon, lat] = g.coordinates;
    if (Number.isFinite(lon) && Number.isFinite(lat)) return [lon, lat];
  }
  return null;
}

const places = []; // [name, lat, lng]
const alias = {}; // normKey -> { idx, weight }
let skipped = 0;

for (const a of ancient) {
  if ((a.types || []).every((t) => SKIP_TYPES.has(t))) {
    skipped++;
    continue;
  }
  const ma = a.modern_associations || {};
  const ids = Object.keys(ma);
  if (!ids.length) {
    skipped++;
    continue;
  }
  const bestId = ids.sort((x, y) => (ma[y].score || 0) - (ma[x].score || 0))[0];
  const pt = pointOf(modernById.get(bestId));
  if (!pt) {
    skipped++;
    continue;
  }
  const [lon, lat] = pt;
  const idx = places.length;
  places.push([a.friendly_id, Math.round(lat * 1e4) / 1e4, Math.round(lon * 1e4) / 1e4]);

  // Weight = how often this place is named in translations (so a common place
  // wins an alias-key collision against a rare one).
  const counts = a.translation_name_counts || {};
  const weight = Object.values(counts).reduce((s, n) => s + n, 0) || 1;
  const names = new Set([a.friendly_id, ...Object.keys(counts)]);
  for (const nm of names) {
    const k = norm(nm);
    if (!k) continue;
    if (!alias[k] || alias[k].weight < weight) alias[k] = { idx, weight };
  }
}

const aliasIdx = Object.fromEntries(Object.entries(alias).map(([k, v]) => [k, v.idx]));

const out = `/**
 * Tanach place gazetteer — generated by scripts/build-gazetteer.mjs from
 * OpenBible's public-domain Bible-Geocoding-Data (CC-BY). DO NOT HAND-EDIT.
 *
 * ${places.length} located places; alias-keyed so a place NAME from the LLM
 * resolves deterministically to {lat,lng}. This is the grounding layer — the
 * model names which places a perek mentions; coordinates come from here.
 */

export interface GazPlace {
  name: string;
  lat: number;
  lng: number;
}

// [name, lat, lng] per place.
const PLACES: [string, number, number][] = ${JSON.stringify(places)};
// normalized-alias -> index into PLACES.
const ALIAS: Record<string, number> = ${JSON.stringify(aliasIdx)};

const norm = (s: string): string => s.toLowerCase().replace(/[^a-z0-9]+/g, '');

/** Resolve a place name (any common spelling) to its coordinate, or null. */
export function lookupPlace(name: string): GazPlace | null {
  const i = ALIAS[norm(name)];
  if (i === undefined) return null;
  const [n, lat, lng] = PLACES[i];
  return { name: n, lat, lng };
}
`;

writeFileSync(new URL('../src/worker/gazetteer.ts', import.meta.url), out);
console.log(
  `wrote src/worker/gazetteer.ts — ${(out.length / 1024).toFixed(0)}KB | ${places.length} places, ${Object.keys(aliasIdx).length} aliases, ${skipped} skipped`,
);
